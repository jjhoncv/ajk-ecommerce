// Obtener lista de departamentos
export const getDepartments = (): string[] => {
  return Object.keys(peruUbigeo).sort()
}

// Obtener provincias de un departamento
export const getProvincesByDepartment = (department: string): string[] => {
  const dept = peruUbigeo[department]
  if (!dept) return []
  return Object.keys(dept).sort()
}

// Obtener distritos de una provincia
export const getDistrictsByProvince = (
  department: string,
  province: string
): string[] => {
  const dept = peruUbigeo[department]
  if (!dept) return []
  const prov = dept[province]
  if (!prov) return []
  return [...prov].sort()
}
